class Ayah {
  final String surahName;
  final String text;
  final int numberInSurah;
  final int juz;
  final int hizbQuarter;
  final bool hasSajda;

  Ayah(
      {required this.surahName,
      required this.text,
      required this.numberInSurah,
      required this.juz,
      required this.hizbQuarter,
      required this.hasSajda});
}
